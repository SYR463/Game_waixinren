# 存储所有的类

class Settings():

    def __init__(self):
        """ 初始化游戏的设置 """
         # 屏幕设置
        self.screen_width = 1200
        self.screen_height = 800
        self.bg_color = (230,230,230)

        # 飞船速度设置
        self.ship_speed_factor = 1.5

        # 子弹设置
        self.bullet.speed.factor = 1
        self.bullet.width = 3
        self.bullet.height = 15
        self.bullet.bgcolor = (50,50,50)


